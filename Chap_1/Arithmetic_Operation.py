# Arithmetic Operator in Python

a = 8
b = 5

# Addition: Adds two operands.
c = a + b
print(f"Result of a + b is {c}")

# Subtraction: Subtracts the right operand from the left operand.
c = a - b
print(f"Result of a - b is {c}")

# Multiplication:  Multiplies two operands.
c = a * b
print(f"Result of a * b is {c}")

# Division: Divides the left operand by the right operand.
# In Python 3, this operation always returns a floating-point number.
c = a / b
print(f"Result of a / b is {c}")

# Floor Division: performs division where the result is rounded down to the nearest integer.
# It truncates the decimal part of the result.
c = a // b
print(f"Result of a // b is {c}")

# Modulus
c = a % b
print(f"Result of a % b is {c}")

# Exponentiation: Raises the left operand to the power of the right operand.
c = a ** b
print(f"Result of a ** b is {c}")
